namespace LionFire.Blazor.Components.MudBlazor_;

/// <summary>
/// Size options for InlineSelector component items
/// </summary>
public enum InlineSelectorSize
{
    /// <summary>
    /// Medium size buttons (default)
    /// </summary>
    Medium,

    /// <summary>
    /// Large MudBlazor button size
    /// </summary>
    Large,

    /// <summary>
    /// Small MudBlazor button size
    /// </summary>
    Small,

    /// <summary>
    /// Compact size, smaller than MudBlazor's small
    /// </summary>
    Compact,

    /// <summary>
    /// Abbreviated text (configurable max characters, default 3)
    /// </summary>
    Abbreviated,

    /// <summary>
    /// Single letter (first character only)
    /// </summary>
    SingleLetter,

    /// <summary>
    /// Colored squares with no text
    /// </summary>
    Square
}
